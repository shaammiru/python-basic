# int to float
x = 1
y = float(x)
print(y)

# float to int
x = 1.5
y = int(x)
print(y)

# number to string
x = 1
y = float(x)
str1 = str(x)
str2 = str(y)
print(str1, str2)

# string to number
x = "1"
y = int(x)
z = float(x)
print(y, z)

# Data set conversion
print(set([1,2,3]))
print(tuple({5,6,7}))
print(list("hello"))

# Dictionary conversion
print(dict([[1,2],[3,4]]))