# int
x = 6
print(type(x))

# float
y = 6.0
print(type(y))

# complex
z = 1+2j
print(type(z))

# string
a = "Hello, World!"
print(type(a))

# boolean
b = True
print(type(b))

# list
c = [1, 2, 3]
print(type(c))

# tuple
d = (1, 2, 3)
print(type(d))

# set
e = {1, 2, 3}
print(type(e))

# dictionary
f = {"name": "John", "age": 36}
print(type(f))